#pragma once

/**
 * This functions checks if value is equal or
 * very close to zero
 * 
 * @param [in] val Value for checking
 * @return Is value equal or close to zero
 */
bool is_zero(double val);

/**
 * This functions checks if values are equal
 * or very close
 * 
 * @param [in] a First value for checking
 * @param [in] b Second value for checking
 * @return Is values are equal or very close 
 */
bool is_equal(double a, double b);
